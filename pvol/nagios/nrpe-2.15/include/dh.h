#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0x81,0x59,0x41,0x15,0x80,0xE9,0xC4,0x16,0xDA,0x26,0xE0,0xC6,
		0x75,0xE8,0x0B,0x7A,0xC8,0x5A,0x4F,0xF1,0x98,0xB3,0x0C,0xBA,
		0x57,0x2D,0x61,0xA8,0x23,0x82,0xA4,0xF9,0x37,0xDB,0x5C,0x08,
		0x88,0x21,0x1F,0xEF,0xBE,0x41,0xFE,0x3D,0x1A,0x58,0xBD,0x53,
		0xB3,0x46,0xE1,0xB7,0xEB,0xCA,0x39,0xAA,0x13,0xC8,0x96,0x37,
		0x0B,0x31,0x35,0x4B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
